   Recipients cannot see other public keys so messages preserve <strong>pseudonimity</strong>.<br />
   New messages <strong>reveal nothing</strong> about existing or future messages.<br />
   The sender can <strong>add new recipients</strong> after the initial encryption process.
  </p>

