   The public content can be generated by any static site generator such as <DefLink url={"https://www.gatsbyjs.com/"} text={"Gatsby"} /> or <DefLink url={"https://jekyllrb.com/"} text={"Jekyll"} />.<br />
   Senders and recipients will need to manage their own keypairs where the content is created and read.
